package com.wuchenyv1990.cloud.k8s.svc.common.domain.model;

public class BC {

    private String b;

    private String c;

    public BC() {
    }

    public BC(String b, String c) {
        this.b = b;
        this.c = c;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

}
